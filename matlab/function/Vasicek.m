function[a,mur,sigmar]=Vasicek(alpha,beta,MSE,P,k,rt)
%QノAR(1)┮oぇ把计@鞍鸭瓢_l雀靴XVasicek家┮惠把计alpha,beta,RMSEAR(1)害舡p把计,POち澄计,kOdata寒啥×`,rtOQv杭衰
L=1;%pdf_l
dt=1/12;%QvCる跑笆
%ㄌ沮AR(1)焊臧T崩旧把计_l
RMSE=MSE^0.5;
sigmar=RMSE/(dt^0.5);
a=(1-beta)/dt;
mur=alpha/(a*dt);
%Na,mu,sigmaq把计_l泉0.01卡10堪档ザ‘妞脸委x}
%auXb单丁jち澄,muuYb,sigmauZb
apre1=linspace(0.01*a,10*a,P);
apre2=repmat(apre1',1,P);
aM=repmat(apre2,1,1,P);
murpre1=linspace(0.01*mur,10*mur,P);
murpre2=repmat(murpre1,P,1);
murM=repmat(murpre2,1,1,P);
sigmarpre1=linspace(0.01*sigmar,10*sigmar,P);
sigmarpre2=reshape(sigmarpre1,1,1,P);
sigmarpre3=repmat(sigmarpre2,1,P,1);
sigmarM=repmat(sigmarpre3,P,1,1);
%NJ程j阀ㄧ计
for i=1:k-1
    Z=((rt(i+1,1)-rt(i,1))-(aM.*(murM-rt(i,1)).*dt))./(2.*((sigmarM.^2).*dt));
    pdf=1./((2.*pi.*(sigmarM.^2).*dt).^0.5).*exp(-(Z.^2)/2);
    if i>1
        L=L.*pdf;
    else
        L=pdf;
    end
end
%D程j阀ㄧ计俺踏j瑟害U影鸭,Y皑膝为喊鸭痞舡p
where=max(max(max(L)));
index=find(L==where);
[rowr,columnr,third]=ind2sub([P,P,P],index);
%块XVasicek家氦T影鸭
a=aM(rowr,columnr,third);
mur=murM(rowr,columnr,third);
sigmar=sigmarM(rowr,columnr,third);